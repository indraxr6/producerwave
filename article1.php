          <h1 style="color: #4CAF50;">
              What Can You do with DAW?
          </h1>
          <figure class="figure">
            <figcaption style="color: whitesmoke; text-align: justify;" class="figure-caption"> In general, there are four main functions of a DAW that at one time were held in separate programs. Nowadays, these four things are packaged into most DAWs:
                <div class="article1-list">
                    <li>Digital audio processor (record, edit, and mix audio digitally)</li>
                    <li>MIDI sequencer (record, edit and mix MIDI notes)</li>
                    <li>Virtual instruments (receives MIDI info and translates it to different instrument sounds)</li>
                    <li>Music notation (turn MIDI notes into printable sheet music)</li>
                </div>
            </figcaption>
          </figure>
